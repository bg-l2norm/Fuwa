class AxolotlAnimation:
    def __init__(self):
        self.frame_index = 0
        self.mood = "NORMAL"

    def set_mood(self, mood: str) -> None:
        mood = mood.upper()
        if mood in MOOD_FRAMES:
            self.mood = mood

    def next_frame(self) -> str:
        frames = MOOD_FRAMES.get(self.mood, MOOD_FRAMES["NORMAL"])
        frame = frames[self.frame_index % len(frames)]
        self.frame_index += 1
        return frame
